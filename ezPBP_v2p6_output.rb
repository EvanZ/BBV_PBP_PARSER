class Game
    attr_reader :game_id,:num_plays,:num_matchups,
    :plays, :matchups,:home_team,:away_team
    attr_writer :game_id,:num_plays,:num_matchups,
    :plays, :matchups,:home_team,:away_team
    def initialize()
        @game_id=nil
        @num_plays=0
        @plays=[]
        @num_matchups=0
        @matchups=[]
        @home_team=nil
        @away_team=nil
    end
    
    def add_play(play,line_no)
            @plays[line_no-1]=play
            @num_plays+=1
    end
    
    def add_matchup(matchup)
        @matchups[@num_matchups]=matchup
        @num_matchups+=1
    end
    
    def parse(player_array, player_hash)
        m=0
        last_foul=nil
        @home_team=@matchups[m].home_team
        @away_team=@matchups[m].away_team
        0.upto(@num_plays) do
            |i|
            if @plays[i] then
            	if i==0 then
                    player_array[$player_hash[@matchups[m].home_player1_name+":"+@home_team]].gs += 1
                    player_array[$player_hash[@matchups[m].home_player2_name+":"+@home_team]].gs += 1
                    player_array[$player_hash[@matchups[m].home_player3_name+":"+@home_team]].gs += 1
                    player_array[$player_hash[@matchups[m].home_player4_name+":"+@home_team]].gs += 1
                    player_array[$player_hash[@matchups[m].home_player5_name+":"+@home_team]].gs += 1
                    player_array[$player_hash[@matchups[m].away_player1_name+":"+@away_team]].gs += 1
                    player_array[$player_hash[@matchups[m].away_player2_name+":"+@away_team]].gs += 1
                    player_array[$player_hash[@matchups[m].away_player3_name+":"+@away_team]].gs += 1
                    player_array[$player_hash[@matchups[m].away_player4_name+":"+@away_team]].gs += 1
                    player_array[$player_hash[@matchups[m].away_player5_name+":"+@away_team]].gs += 1
				end            		
                play=@plays[i] 
                play_time=play[2] 
                result=play[3] 
                if play_time < @matchups[m].end_time and !play_time.match("-") then
                    player_array[$player_hash[@matchups[m].home_player1_name+":"+@home_team]].pos1 += @matchups[m].pos_home
                    player_array[$player_hash[@matchups[m].home_player2_name+":"+@home_team]].pos2 += @matchups[m].pos_home
                    player_array[$player_hash[@matchups[m].home_player3_name+":"+@home_team]].pos3 += @matchups[m].pos_home
                    player_array[$player_hash[@matchups[m].home_player4_name+":"+@home_team]].pos4 += @matchups[m].pos_home
                    player_array[$player_hash[@matchups[m].home_player5_name+":"+@home_team]].pos5 += @matchups[m].pos_home
                    player_array[$player_hash[@matchups[m].away_player1_name+":"+@away_team]].pos1 += @matchups[m].pos_away
                    player_array[$player_hash[@matchups[m].away_player2_name+":"+@away_team]].pos2 += @matchups[m].pos_away
                    player_array[$player_hash[@matchups[m].away_player3_name+":"+@away_team]].pos3 += @matchups[m].pos_away
                    player_array[$player_hash[@matchups[m].away_player4_name+":"+@away_team]].pos4 += @matchups[m].pos_away
                    player_array[$player_hash[@matchups[m].away_player5_name+":"+@away_team]].pos5 += @matchups[m].pos_away
                    m += 1
                elsif play_time.match("-") and play_time > @matchups[m].end_time then
                    player_array[$player_hash[@matchups[m].home_player1_name+":"+@home_team]].pos1 += @matchups[m].pos_home
                    player_array[$player_hash[@matchups[m].home_player2_name+":"+@home_team]].pos2 += @matchups[m].pos_home
                    player_array[$player_hash[@matchups[m].home_player3_name+":"+@home_team]].pos3 += @matchups[m].pos_home
                    player_array[$player_hash[@matchups[m].home_player4_name+":"+@home_team]].pos4 += @matchups[m].pos_home
                    player_array[$player_hash[@matchups[m].home_player5_name+":"+@home_team]].pos5 += @matchups[m].pos_home
